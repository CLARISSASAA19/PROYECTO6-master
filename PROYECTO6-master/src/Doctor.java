public class Doctor {
    private String name;
    private String doctor;

    public Doctor(String name) {
        this.name = name;
        this.doctor=doctor;
    }

    public String getName() {
        return name;
    }

    public String getDoctor() {
        return doctor;
    }

    public void setDoctor(String doctor) {
        this.doctor = doctor;
    }
}